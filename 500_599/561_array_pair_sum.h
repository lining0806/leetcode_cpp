#ifndef _61_array_pair_sum_h
#define _61_array_pair_sum_h

class Solution {
public:
    int arrayPairSum(vector<int>& nums) {
        int res = 0, n = nums.size();
        sort(nums.begin(), nums.end());
        for (int i = 0; i < n; i += 2) {
            res += nums[i];
        }
        return res;
    }
};

#endif /* _61_array_pair_sum_h */
