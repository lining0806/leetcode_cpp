#ifndef _75_distribute_candies_h
#define _75_distribute_candies_h

class Solution {
public:
    int distributeCandies(vector<int>& candies) {
        unordered_set<int> s;
        for (int candy : candies) s.insert(candy);
        return min(s.size(), candies.size() / 2);
    }
};

#endif /* _75_distribute_candies_h */
