#ifndef _20_minimum_length_encoding_h
#define _20_minimum_length_encoding_h

class Solution {
public:
    int minimumLengthEncoding(vector<string>& words) {
        unordered_set<string> s(words.begin(), words.end());
        for (string w : s)
            for (int i = 1; i < w.size(); ++i)
                s.erase(w.substr(i));
        int res = 0;
        for (string w : s) res += w.size() + 1;
        return res;
    }
};

#endif /* _20_minimum_length_encoding_h */
