#ifndef _96_rotate_string_h
#define _96_rotate_string_h

class Solution {
public:
    bool rotateString(string A, string B) {
        return A.size() == B.size() && (A + A).find(B) != string::npos;
    }
};

#endif /* _96_rotate_string_h */
